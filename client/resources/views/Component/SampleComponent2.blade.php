    <script>
        PillsCategory('/category-by-id/12','#HealthPills','HealthItem')
        $('#HealthPills').on('click','.HealthItem',function (){
            $('.HealthItem').removeClass('disabled')
            $(this).addClass('disabled')
            let id = $(this).attr('SubCategoryID');
            if(id === "0"){
                AllHealthNews();
            }else{
                GetHealthNews(`/get-all-news/${id}/lead_news/1/sub`);
                GetHealthSubNews(`/get-all-news/${id}/side_bar_news/2/sub`)
            }
        })
        AllHealthNews();
        function AllHealthNews(){
            GetHealthNews('/get-all-news/12/lead_news/1');
            GetHealthSubNews('/get-all-news/12/side_bar_news/2');
        }
        function GetHealthNews(url){
            GetData(url, function (response){
                if(response.status === 200 ){
                    let data = response.data;
                    let order = 1;
                    $('#HealthLeadNews').empty();
                    for(let i = 0; i < data.length; i++){
                        for(let j = 0; j < order; j++){
                            if(data[i].order == j+1){
                                HealthLeadNews(data[i].image,data[i].title)
                            }
                        }
                    }            }
            });
        }
        function HealthLeadNews(image,title){
            $('#HealthLeadNews').append(`
                <div class="card" >
                    <img src="${image}" class="card-img">
                    <div class="card-body">
                        <h5>${title}</h5>
                    </div>
                </div>
            `)
        }
        function GetHealthSubNews(url){
            GetData(url, function (response){
                if(response.status === 200 ){
                    let data = response.data;
                    let order = 2;
                    $('#HealthSubNews').empty();
                    for(let i = 0; i < data.length; i++){
                        for(let j = 0; j < order; j++){
                            if(data[i].order == j+1){
                                HeathSubNews(data[i].image,data[i].title,data[i].date)
                            }
                        }
                    }            }
            });
        }
        function HeathSubNews(image,title,time){
            $('#HealthSubNews').append(`
                 <a href="#" class="news link border-bottom">
                        <img class="image" style="height: 70px" src="${image}">
                        <div>
                            <h5 class="title m-0 line-2" style="margin-bottom: 5px!important;">${title}</h5>
                            <div class="hour"><i class="fas  fa-clock" style="margin: 0 5px 0 0;"></i>${site.localeDate(time)}</div>
                        </div>
                    </a>
            `)
        }

    </script>
